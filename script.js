function displayData(data) {
  const contentDiv = document.getElementById('content');
  contentDiv.innerHTML = ''; // Effacer le contenu précédent
  data.forEach(item => {
    const div = document.createElement('div');
    div.classList.add('item');
    let imageHTML = '';
    if (item.Image) {
      imageHTML = `<img src="images/${item.Image}" alt="${item.Nom} ${item.Prénom}">`;
    }
    div.innerHTML = `
      ${imageHTML}
      <strong>${item.Nom} ${item.Prénom}</strong> - ${item.Fonction}
      <br><em>${item.Institution}</em>
      <p>${item.Présentation}</p>
    `;
    contentDiv.appendChild(div);
  });
}

// Fonction pour filtrer les données par thématique
function filterCategory(category) {
  const contentDiv = document.getElementById('content');
  contentDiv.className = ''; // Effacer toutes les classes existantes
  contentDiv.classList.add(`category-${category.toLowerCase().replace(/ /g, '-')}`);
  const filteredData = thématiques[category] || data;
  displayData(filteredData);
}

